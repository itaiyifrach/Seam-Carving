public class EnergyData {

    private double energyValue;
    private double cumulatedEnergyValueUntilMe;

    public EnergyData(){}

    public double getEnergyValue() {
        return energyValue;
    }

    public void setEnergyValue(double energyValue) {
        this.energyValue = energyValue;
    }

    public double getCumulatedEnergyValueUntilMe() {
        return cumulatedEnergyValueUntilMe;
    }

    public void setCumulatedEnergyValueUntilMe(double cumulatedEnergyValueUntilMe) {
        this.cumulatedEnergyValueUntilMe = cumulatedEnergyValueUntilMe;
    }




}
